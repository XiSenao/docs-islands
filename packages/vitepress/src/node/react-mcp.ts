type JsonRpcId = number | string | null;

interface JsonRpcRequest {
  id?: JsonRpcId;
  jsonrpc?: string;
  method: string;
  params?: unknown;
}

interface JsonRpcResponse {
  error?: {
    code: number;
    message: string;
  };
  id: JsonRpcId;
  jsonrpc: '2.0';
  result?: unknown;
}

export type SiteDebugBuildMcpToolName =
  | 'get_artifact'
  | 'get_build_overview'
  | 'get_build_report'
  | 'get_component'
  | 'get_page'
  | 'list_pages';

export interface SiteDebugBuildMcpServerOptions
  extends SiteDebugBuildDataOptions {
  stderr?: Writable;
  stdin?: NodeJS.ReadableStream;
  stdout?: Writable;
}

type SiteDebugBuildMcpToolResult =
  | SiteDebugBuildArtifactSnapshot
  | SiteDebugBuildComponentSummary
  | {
      error: string;
    }
  | SiteDebugBuildOverview
  | SiteDebugBuildPageSummary[]
  | SiteDebugBuildReportPayload
  | {
      page: unknown;
      summary: SiteDebugBuildPageSummary;
    };

interface SiteDebugBuildMcpResource {
  description: string;
  mimeType: 'text/markdown';
  name: string;
  text: string;
  uri: string;
}

