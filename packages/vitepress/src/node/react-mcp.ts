const isRecord = (value: unknown): value is Record<string, unknown> =>
  Boolean(value && typeof value === 'object' && !Array.isArray(value));

const createJsonRpcErrorResponse = (
  id: JsonRpcId,
  code: number,
  message: string,
): JsonRpcResponse => ({
  error: {
    code,
    message,
  },
  id,
  jsonrpc: '2.0',
});

const createJsonRpcSuccessResponse = (
  id: JsonRpcId,
  result: unknown,
): JsonRpcResponse => ({
  id,
  jsonrpc: '2.0',
  result,
});

const serializeToolResult = (
  result: SiteDebugBuildMcpToolResult,
  isError = false,
) => ({
  content: [
    {
      text: JSON.stringify(result, null, 2),
      type: 'text',
    },
  ],
  ...(isError ? { isError: true } : {}),
  structuredContent: result,
});

const getRequiredString = (
  value: Record<string, unknown>,
  key: string,
): string => {
  const nextValue = value[key];

  if (typeof nextValue !== 'string' || nextValue.length === 0) {
    throw new Error(`"${key}" must be a non-empty string.`);
  }

  return nextValue;
};

const getOptionalString = (
  value: Record<string, unknown>,
  key: string,
): string | undefined => {
  const nextValue = value[key];

  if (nextValue === undefined) {
    return undefined;
  }

  if (typeof nextValue !== 'string' || nextValue.length === 0) {
    throw new Error(`"${key}" must be a non-empty string when provided.`);
  }

  return nextValue;
};

const createToolArgumentErrorResult = (message: string) =>
  serializeToolResult(
    {
      error: message,
    } as { error: string },
    true,
  );

const parseCliArgs = (argv: string[]): SiteDebugBuildDataOptions | null => {
  const nextOptions: SiteDebugBuildDataOptions = {
    outDir: '',
  };

  for (let index = 0; index < argv.length; index += 1) {
    const argument = argv[index];

    if (argument === '--help') {
      return null;
    }

    if (argument === '--outDir') {
      nextOptions.outDir = argv[index + 1] || '';
      index += 1;
      continue;
    }

    if (argument === '--assetsDir') {
      nextOptions.assetsDir = argv[index + 1] || '';
      index += 1;
      continue;
    }

    throw new Error(`Unknown argument: ${argument}`);
  }

  if (!nextOptions.outDir) {
    throw new Error('Missing required argument: --outDir');
  }

  return nextOptions;
};

export interface SiteDebugBuildMcpServer {
  close: () => void;
  handleMessage: (message: unknown) => Promise<JsonRpcResponse | null>;
  listen: () => void;
}

export const createSiteDebugBuildMcpServer = (
  options: SiteDebugBuildMcpServerOptions,
): SiteDebugBuildMcpServer => {
  const dataStore = new SiteDebugBuildDataStore(options);
  const stdin = options.stdin || process.stdin;
  const stdout = options.stdout || process.stdout;
  const stderr = options.stderr || process.stderr;
  let buffer = Buffer.alloc(0);
  let closed = false;

  const writeMessage = (message: JsonRpcResponse) => {
    if (closed) {
      return;
    }

    const payload = Buffer.from(JSON.stringify(message), 'utf8');
    stdout.write(
      `Content-Length: ${payload.byteLength}\r\nContent-Type: application/json\r\n\r\n`,
    );
    stdout.write(payload);
  };

  const handleToolCall = async (
    name: SiteDebugBuildMcpToolName,
    args: unknown,
  ) => {
    const normalizedArgs = isRecord(args) ? args : {};

    try {
      switch (name) {
        case 'get_build_overview': {
          return serializeToolResult(dataStore.getBuildOverview());
        }
        case 'list_pages': {
          return serializeToolResult(dataStore.listPages());
        }
        case 'get_page': {
          return serializeToolResult(
            dataStore.getPage(getRequiredString(normalizedArgs, 'pageId')),
          );
        }
        case 'get_component': {
          return serializeToolResult(
            dataStore.getComponent(
              getRequiredString(normalizedArgs, 'pageId'),
              getRequiredString(normalizedArgs, 'componentName'),
            ),
          );
        }
        case 'get_artifact': {
          const artifactKind = getRequiredString(
            normalizedArgs,
            'artifactKind',
          );

          if (
            artifactKind !== 'bundle-chunk' &&
            artifactKind !== 'bundle-module'
          ) {
            throw new Error(
              '"artifactKind" must be "bundle-chunk" or "bundle-module".',
            );
          }

          return serializeToolResult(
            dataStore.getArtifact({
              artifactKind,
              displayPath: getOptionalString(normalizedArgs, 'displayPath'),
              file: getOptionalString(normalizedArgs, 'file'),
              moduleId: getOptionalString(normalizedArgs, 'moduleId'),
            }),
          );
        }
        case 'get_build_report': {
          const artifactKey = getOptionalString(normalizedArgs, 'artifactKey');
          const reportId = getOptionalString(normalizedArgs, 'reportId');

          if (!artifactKey && !reportId) {
            throw new Error(
              'Provide at least one of "reportId" or "artifactKey".',
            );
          }

          return serializeToolResult(
            dataStore.getBuildReport({
              artifactKey,
              reportId,
            }),
          );
        }
        default: {
          return createToolArgumentErrorResult(`Unsupported tool: ${name}`);
        }
      }
    } catch (error) {
      return createToolArgumentErrorResult(
        error instanceof Error ? error.message : 'Tool execution failed.',
      );
    }
  };

  const handleRequest = async (
    request: JsonRpcRequest,
  ): Promise<JsonRpcResponse | null> => {
    const id = request.id ?? null;

    switch (request.method) {
      case 'initialize': {
        return createJsonRpcSuccessResponse(id, {
          capabilities: {
            resources: {
              listChanged: false,
              subscribe: false,
            },
            tools: {
              listChanged: false,
            },
          },
          protocolVersion: PROTOCOL_VERSION,
          serverInfo: {
            name: SERVER_NAME,
            version: SERVER_VERSION,
          },
        });
      }
      case 'notifications/initialized': {
        return null;
      }
      case 'ping': {
        return createJsonRpcSuccessResponse(id, {});
      }
      case 'tools/list': {
        return createJsonRpcSuccessResponse(id, {
          tools: MCP_TOOLS,
        });
      }
      case 'tools/call': {
        const params = isRecord(request.params) ? request.params : {};
        const toolName = getRequiredString(params, 'name');
        const tool = MCP_TOOLS.find((item) => item.name === toolName);

        if (!tool) {
          return createJsonRpcSuccessResponse(
            id,
            createToolArgumentErrorResult(`Unknown tool: ${toolName}`),
          );
        }

        return createJsonRpcSuccessResponse(
          id,
          await handleToolCall(tool.name, params.arguments),
        );
      }
      case 'resources/list': {
        return createJsonRpcSuccessResponse(id, {
          resources: MCP_RESOURCES.map((resource) => ({
            description: resource.description,
            mimeType: resource.mimeType,
            name: resource.name,
            uri: resource.uri,
          })),
        });
      }
      case 'resources/read': {
        const params = isRecord(request.params) ? request.params : {};
        const resourceUri = getRequiredString(params, 'uri');
        const resource = MCP_RESOURCES.find((item) => item.uri === resourceUri);

        if (!resource) {
          return createJsonRpcErrorResponse(
            id,
            -32_602,
            `Unknown resource URI: ${resourceUri}`,
          );
        }

        return createJsonRpcSuccessResponse(id, {
          contents: [
            {
              mimeType: resource.mimeType,
              text: resource.text,
              uri: resource.uri,
            },
          ],
        });
      }
      case 'shutdown': {
        return createJsonRpcSuccessResponse(id, null);
      }
      case 'exit': {
        closed = true;
        return null;
      }
      default: {
        if (request.id === undefined) {
          return null;
        }

        return createJsonRpcErrorResponse(
          id,
          -32_601,
          `Method not found: ${request.method}`,
        );
      }
    }
  };

  const flushBuffer = async () => {
    while (true) {
      const headerEnd = buffer.indexOf('\r\n\r\n');

      if (headerEnd === -1) {
        return;
      }

      const headerText = buffer.slice(0, headerEnd).toString('utf8');
      const headers = Object.fromEntries(
        headerText
          .split('\r\n')
          .map((line) => line.split(/:\s*/, 2))
          .filter(
            (entry): entry is [string, string] =>
              entry.length === 2 &&
              typeof entry[0] === 'string' &&
              typeof entry[1] === 'string',
          )
          .map(([key, value]) => [key.toLowerCase(), value]),
      );
      const contentLength = Number.parseInt(headers['content-length'], 10);

      if (!Number.isFinite(contentLength) || contentLength < 0) {
        throw new Error('Missing or invalid Content-Length header.');
      }

      const messageStart = headerEnd + 4;
      const messageEnd = messageStart + contentLength;

      if (buffer.byteLength < messageEnd) {
        return;
      }

      const payload = buffer.slice(messageStart, messageEnd).toString('utf8');
      buffer = buffer.slice(messageEnd);

      const message = JSON.parse(payload) as JsonRpcRequest;
      const response = await handleRequest(message);

      if (response) {
        writeMessage(response);
      }
    }
  };

  let pendingFlush = Promise.resolve();

  return {
    close() {
      closed = true;
    },
    async handleMessage(message: unknown) {
      if (!isRecord(message) || typeof message.method !== 'string') {
        return createJsonRpcErrorResponse(null, -32_600, 'Invalid request.');
      }

      const normalizedMessage = message as unknown as JsonRpcRequest;

      try {
        return await handleRequest(normalizedMessage);
      } catch (error) {
        return createJsonRpcErrorResponse(
          normalizedMessage.id ?? null,
          -32_603,
          error instanceof Error ? error.message : 'Internal error.',
        );
      }
    },
    listen() {
      stdin.on('data', (chunk: Buffer | string) => {
        buffer = Buffer.concat([
          buffer,
          Buffer.isBuffer(chunk) ? chunk : Buffer.from(chunk),
        ]);
        pendingFlush = pendingFlush
          .then(() => flushBuffer())
          .catch((error) => {
            stderr.write(
              `${error instanceof Error ? error.message : 'MCP transport error.'}\n`,
            );
          });
      });
    },
  };
};

export const startSiteDebugBuildMcpServer = (
  options: SiteDebugBuildMcpServerOptions,
): SiteDebugBuildMcpServer => {
  const server = createSiteDebugBuildMcpServer(options);
  server.listen();
  return server;
};

export const runSiteDebugBuildMcpCli = async (
  argv: string[] = process.argv.slice(2),
): Promise<void> => {
  try {
    const options = parseCliArgs(argv);

    if (!options) {
      process.stdout.write(`${HELP_TEXT}\n`);
      return;
    }

    startSiteDebugBuildMcpServer(options);
  } catch (error) {
    process.stderr.write(
      `${error instanceof Error ? error.message : 'Failed to start MCP server.'}\n`,
    );
    process.stderr.write(`${HELP_TEXT}\n`);
    process.exitCode = 1;
  }
};

const currentFilePath = fileURLToPath(import.meta.url);

if (process.argv[1] && process.argv[1] === currentFilePath) {
  runSiteDebugBuildMcpCli().catch((error) => {
    process.stderr.write(
      `${error instanceof Error ? error.message : 'Failed to start MCP server.'}\n`,
    );
    process.exitCode = 1;
  });
}

